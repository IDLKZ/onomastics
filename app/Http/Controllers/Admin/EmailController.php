    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $email = Email::find($id);
        if($email){
            $email->delete();
            toastr()->success(__("messages.deleted"));
        }
        return redirect()->route("admin-email.index");
    }
